export function isBadDemoImage(src?: string | null) {
	if (!src) return true;
	return (
		/^data:image\/svg/i.test(src) ||
		/loremflickr\.com|source\.unsplash\.com/i.test(src) ||
		src.includes("photo-1575650772417-e6b418b0d106") ||
		src.includes("photo-1614313511387-1436a4480ebb")
	);
}

export function productPhotoUrl(name: string, category?: string | null) {
	const normalized = name.trim().toLowerCase();
	return (
		PHOTO_BY_NAME[normalized] ??
		PHOTO_BY_CATEGORY[category ?? ""] ??
		PHOTO_BY_CATEGORY.servicios
	);
}

